import { useEffect, useState } from 'react';
import { X, CheckCircle, XCircle, FileText } from 'lucide-react';
import clsx from 'clsx';
import type { IterationRunArtifact, LoadIterationRunRequest } from '@nakiros/shared';
import {
  ConversationTurn,
  legacyTurnToBlocks,
} from '../ConversationTurn';

/**
 * Slide-in drawer that loads and displays the full artefact of a single
 * eval run (one cell of the matrix). Contents are fetched lazily when the
 * drawer opens so the matrix itself stays lightweight.
 *
 * Sections:
 *  - Header: eval name / iter / config / status + close button
 *  - Assertions: each assertion with pass/fail + judge evidence
 *  - Conversation: turns with interleaved text/tool blocks
 *  - Outputs: list of files generated by the agent
 *  - Diff: git diff of sandbox changes (if git-worktree mode was used)
 */
export function EvalMatrixDrawer({
  request,
  onClose,
}: {
  request: LoadIterationRunRequest | null;
  onClose(): void;
}) {
  const [artifact, setArtifact] = useState<IterationRunArtifact | null>(null);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    if (!request) return;
    let mounted = true;
    setLoading(true);
    setError(null);
    setArtifact(null);
    window.nakiros
      .loadIterationRun(request)
      .then((data) => {
        if (mounted) setArtifact(data);
      })
      .catch((err: unknown) => {
        if (mounted) setError((err as Error).message ?? String(err));
      })
      .finally(() => {
        if (mounted) setLoading(false);
      });
    return () => {
      mounted = false;
    };
  }, [request]);

  if (!request) return null;

  return (
    <div className="fixed inset-0 z-[400] flex">
      {/* Backdrop */}
      <div
        onClick={onClose}
        className="flex-1 bg-black/40 transition-opacity"
        aria-label="Close drawer"
      />
      {/* Drawer panel */}
      <div className="flex w-full max-w-[780px] flex-col border-l border-[var(--line)] bg-[var(--bg)] shadow-2xl">
        {/* Header */}
        <div className="flex shrink-0 items-center gap-3 border-b border-[var(--line)] bg-[var(--bg-soft)] px-4 py-3">
          <div className="flex-1">
            <div className="text-sm font-semibold text-[var(--text-primary)]">
              {request.evalName}
            </div>
            <div className="flex gap-2 text-xs text-[var(--text-muted)]">
              <span>iter {request.iteration}</span>
              <span>·</span>
              <span>{request.config === 'with_skill' ? 'with skill' : 'baseline'}</span>
              {artifact?.grading && (
                <>
                  <span>·</span>
                  <span className="font-semibold text-[var(--text-primary)]">
                    {artifact.grading.summary.passed}/{artifact.grading.summary.total} assertions
                  </span>
                </>
              )}
              {artifact?.timing && (
                <>
                  <span>·</span>
                  <span>{formatTokens(artifact.timing.totalTokens)} tokens</span>
                  <span>·</span>
                  <span>{formatDuration(artifact.timing.durationMs)}</span>
                </>
              )}
            </div>
          </div>
          <button
            onClick={onClose}
            className="rounded p-1 text-[var(--text-muted)] transition-colors hover:bg-[var(--bg-muted)] hover:text-[var(--text-primary)]"
          >
            <X size={16} />
          </button>
        </div>

        {/* Body */}
        <div className="flex-1 overflow-y-auto p-4">
          {loading && <div className="text-xs text-[var(--text-muted)]">Loading…</div>}
          {error && (
            <div className="rounded border border-red-500/30 bg-red-500/10 px-3 py-2 text-xs text-red-400">
              {error}
            </div>
          )}

          {artifact && (
            <div className="flex flex-col gap-6">
              {/* Assertions */}
              {artifact.grading && artifact.grading.assertion_results.length > 0 && (
                <section>
                  <h3 className="mb-2 text-xs font-semibold uppercase tracking-wide text-[var(--text-muted)]">
                    Assertions
                  </h3>
                  <ul className="flex flex-col gap-2">
                    {artifact.grading.assertion_results.map((a, i) => (
                      <li
                        key={i}
                        className={clsx(
                          'rounded-lg border px-3 py-2',
                          a.passed
                            ? 'border-emerald-500/30 bg-emerald-500/5'
                            : 'border-red-500/30 bg-red-500/5',
                        )}
                      >
                        <div className="mb-1 flex items-start gap-2">
                          {a.passed ? (
                            <CheckCircle size={14} className="mt-0.5 shrink-0 text-emerald-400" />
                          ) : (
                            <XCircle size={14} className="mt-0.5 shrink-0 text-red-400" />
                          )}
                          <span className="text-xs font-medium text-[var(--text-primary)]">{a.text}</span>
                          <span className="ml-auto shrink-0 rounded bg-[var(--bg-muted)] px-1.5 py-0.5 text-[9px] font-bold text-[var(--text-muted)]">
                            {a.type}
                          </span>
                        </div>
                        {a.evidence && (
                          <div className="ml-6 whitespace-pre-wrap rounded bg-[var(--bg-muted)] px-2 py-1 font-mono text-[11px] text-[var(--text-muted)]">
                            {a.evidence}
                          </div>
                        )}
                      </li>
                    ))}
                  </ul>
                </section>
              )}

              {/* Conversation */}
              {artifact.run && artifact.run.turns.length > 0 && (
                <section>
                  <h3 className="mb-2 text-xs font-semibold uppercase tracking-wide text-[var(--text-muted)]">
                    Conversation
                  </h3>
                  <div className="flex flex-col gap-2">
                    {artifact.run.turns.map((turn, i) => (
                      <ConversationTurn
                        key={i}
                        role={turn.role}
                        timestamp={turn.timestamp}
                        blocks={
                          turn.blocks ??
                          (turn.role === 'assistant'
                            ? legacyTurnToBlocks(turn.content, turn.tools)
                            : [{ type: 'text', text: turn.content }])
                        }
                      />
                    ))}
                  </div>
                </section>
              )}

              {/* Outputs */}
              {artifact.outputs.length > 0 && (
                <section>
                  <h3 className="mb-2 text-xs font-semibold uppercase tracking-wide text-[var(--text-muted)]">
                    Generated outputs
                  </h3>
                  <ul className="flex flex-col gap-1 text-xs">
                    {artifact.outputs.map((o) => (
                      <li
                        key={o.relativePath}
                        className="flex items-center gap-2 rounded border border-[var(--line)] bg-[var(--bg-card)] px-2 py-1.5"
                      >
                        <FileText size={12} className="text-[var(--text-muted)]" />
                        <span className="font-mono text-[var(--text-primary)]">{o.relativePath}</span>
                        <span className="ml-auto text-[var(--text-muted)]">{formatBytes(o.sizeBytes)}</span>
                      </li>
                    ))}
                  </ul>
                </section>
              )}

              {/* Diff */}
              {artifact.diffPatch && (
                <section>
                  <h3 className="mb-2 text-xs font-semibold uppercase tracking-wide text-[var(--text-muted)]">
                    Sandbox diff
                  </h3>
                  <pre className="max-h-[400px] overflow-auto whitespace-pre rounded bg-[var(--bg-muted)] p-3 font-mono text-[11px] text-[var(--text-primary)]">
                    {artifact.diffPatch}
                  </pre>
                </section>
              )}

              {!artifact.run && !artifact.grading && artifact.outputs.length === 0 && (
                <div className="text-xs text-[var(--text-muted)]">
                  No artefacts found for this run (run may have been deleted or never completed).
                </div>
              )}
            </div>
          )}
        </div>
      </div>
    </div>
  );
}

function formatTokens(n: number): string {
  if (n < 1000) return `${n}`;
  return `${(n / 1000).toFixed(1)}k`;
}

function formatDuration(ms: number): string {
  if (ms < 1000) return `${ms}ms`;
  if (ms < 60000) return `${(ms / 1000).toFixed(1)}s`;
  return `${Math.floor(ms / 60000)}m${Math.floor((ms % 60000) / 1000)}s`;
}

function formatBytes(n: number): string {
  if (n < 1024) return `${n}B`;
  if (n < 1024 * 1024) return `${(n / 1024).toFixed(1)}KB`;
  return `${(n / (1024 * 1024)).toFixed(1)}MB`;
}
